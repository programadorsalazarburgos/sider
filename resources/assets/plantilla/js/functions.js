$.fn.select = function(attr) 
{
    var html='<option value="">Selecione</option>';
    $.each(attr.data,function(index,value)
    {
        html+='<option value="'+value[attr.value]+'">'+value[attr.name]+'</option>';
    })
    $(this).html(html);
};
